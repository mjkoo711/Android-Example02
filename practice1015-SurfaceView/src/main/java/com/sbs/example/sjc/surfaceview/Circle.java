package com.sbs.example.sjc.surfaceview;

public class Circle {
    int x, y;
    int color;
    int speedX, speedY; //속도

    public Circle(int x, int y, int color, int speedX, int speedY) {
        super();
        this.x = x;
        this.y = y;
        this.color = color;
        this.speedX = speedX;
        this.speedY = speedY;
    }

}